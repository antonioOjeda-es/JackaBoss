package operadoresAritmeticos;

public class Comparacion {

    public static void main(String[] args) {

        int a = 10;
        int b = 5;

        System.out.println("A es igual a B? " + (a == b));
        System.out.println("A es distinto a B? " + (a != b));
        System.out.println("A mayor a B? " + (a > b));
        System.out.println("A menor a B? " + (a < b));

        System.out.println("A mayor o igual a B? " + (a >= b));
        System.out.println("A menor o igual a B? " + (a <= b));




    }
}
